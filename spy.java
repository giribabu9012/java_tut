class spy 
{
	public static void main(String[] args) 
	{ 
		int a=132;
		int sum=0;
		int product=1;
		int last=0;
		while(a>0)
		{
			last=a % 10;
			sum=sum+last;
			product=product*last;
			a=a/10;
		}
			if(sum==product)
			{
			System.out.print("It is a spy number");
			}
			else
			{
			System.out.print("It is not a spy number");
			}
	}
}
