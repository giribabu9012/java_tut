class getset 
{
	String name;
	int age;
	String email;
	public getset(String name,int age,String email)
	{
		this.name=name;
		this.age=age;
		this.email=email;
	}
	public String getname()
	{
		return name;
	}
	public int getage()
	{
		return age;
	}
	public String getemail()
	{
		return email;
	}
	public void setname(String name)
	{
		this.name=name;
	}
	public void setage(int age)
	{
		this.age=age;
	}
	public void setemail(String email)
	{
		this.email=email;
	}
}
